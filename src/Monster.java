
abstract class Monster extends Life{
	 void attack() {}

	 public int getExp() {
		return exp;
	}

	public void setExp(int exp) {
		this.exp = exp;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	private int exp;

	private String name;

}
